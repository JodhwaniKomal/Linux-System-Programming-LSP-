int Addition(int No1 ,int No2)
{
    int ans = 0;
    ans = No1 + No2;
    return ans;
}

int Substraction(int No1 ,int No2)
{
    int ans = 0;
    ans = No1 - No2;
    return ans;
}

int Multiplication(int No1 ,int No2)
{
    int ans = 0;
    ans = No1 * No2;
    return ans;
}

int Division(int No1 ,int No2)
{
    int ans = 0;
    ans = No1/No2;
    return ans;
}